public class add
{
    public static int sum(int x, int y){
        return x+y;
    }
    public static int subtract(int x, int y){
        return y-x;
    }

    public static void main(String[] args)
    {
        System.out.println(sum(9, 8));
        System.out.println(subtract(120,10));
    }


}
